
public class SpecialChars {

	public static void main(String[] args) {
		// Declararea de chars se face cu ' '
		char c = (char)104; 
		// \t (tab) \b (bell sound) \v (vertical tab)
		// \r (carriage return) \n (new line / line feed)
		String s = "Ala " + c + " Bala";
		System.out.println(s);
		System.out.println(c);
		
	
	}

}
